package edu.ifsp.ged.commons.ranking;

public class StationRanking extends Ranking {

	private int points;               
	private int pointsBase;           
	private int pointsConvenience;    
	private int pointsAtendense;      
	private int pointsProducts;       
	private int pointsPrice;          
	private int pointsAnalyses;       
	private int pointsRegistrationAge;
	
	public int getPoints() {
		return points;
	}

	public void setPoints(int points) {
		this.points = points;
	}

	public int getPointsBase() {
		return pointsBase;
	}

	public void setPointsBase(int pointsBase) {
		this.pointsBase = pointsBase;
	}

	public int getPointsConvenience() {
		return pointsConvenience;
	}

	public void setPointsConvenience(int pointsConvenience) {
		this.pointsConvenience = pointsConvenience;
	}

	public int getPointsAtendense() {
		return pointsAtendense;
	}

	public void setPointsAtendense(int pointsAtendense) {
		this.pointsAtendense = pointsAtendense;
	}

	public int getPointsProducts() {
		return pointsProducts;
	}

	public void setPointsProducts(int pointsProducts) {
		this.pointsProducts = pointsProducts;
	}

	public int getPointsPrice() {
		return pointsPrice;
	}

	public void setPointsPrice(int pointsPrice) {
		this.pointsPrice = pointsPrice;
	}

	public int getPointsAnalyses() {
		return pointsAnalyses;
	}

	public void setPointsAnalyses(int pointsAnalyses) {
		this.pointsAnalyses = pointsAnalyses;
	}

	public int getPointsRegistrationAge() {
		return pointsRegistrationAge;
	}

	public void setPointsRegistrationAge(int pointsRegistrationAge) {
		this.pointsRegistrationAge = pointsRegistrationAge;
	}
}
